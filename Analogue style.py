def print_ascii_word(word):
    word = word.upper()
    output_rows = [""] * 5

    for char in word:
        if char in letters:
            for i in range(5):
                output_rows[i] += letters[char][i] + "  "
        else:
            print('WRITE ONLY LETTERS NOT ANY SYMBOLS OR SPECIAL CHARECTERS...')
        
    print("\n" + "=" * len(output_rows[0]))
    for row in output_rows:
        print(row)
    print("=" * len(output_rows[0]))

